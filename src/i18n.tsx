export function createTranslator(language: UiLanguage): Translator {
  return (source, values = {}) => {
    const template = language === 'en' ? englishTranslations[source] ?? source : source
    return Object.entries(values).reduce((text, [key, value]) => text.replaceAll(`{${key}}`, String(value)), template)
  }
}

const I18nContext = createContext<{ language: UiLanguage; t: Translator }>({ language: 'ja', t: createTranslator('ja') })

export function I18nProvider({ language, children }: { language: UiLanguage; children: ReactNode }) {
  const value = useMemo(() => ({ language, t: createTranslator(language) }), [language])
  useEffect(() => {
    document.documentElement.lang = language
  }, [language])
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  return useContext(I18nContext)
}
